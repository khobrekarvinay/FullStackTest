

//Q. What is Docker ??

// Docker is a software platform that allows you to build, test, and deploy applications quickly. 
// Docker packages software into standardized units called containers that have everything the software needs to run including libraries, system tools, code, and runtime. 
// Using Docker, you can quickly deploy and scale applications into any environment and know your code will run.



//Q. What are containers?

// Containers provide a way of creating an isolated environment, sometimes called a sandbox, in which applications and their dependencies can live.

// PORTABLE -- Isolated environment means the container is separated from the environment in which they run. Basically they don't care much about the environment in which they run.
// This means they can be run in many different environments with different OS & Hardware platforms.

// CONSISTENCY -- Since the containers are separated from the environment in which they run, you can be sure that they operate the same, regardless of where they are deployed.

