// How it Works ? ----------------

//1. Containers & Pods
// Apps are placed inside containers (like small, self-contained boxes of software).
// Kubernetes groups containers into Pods (the smallest unit in Kubernetes).

// 2. Automated Scaling
// If traffic increases, Kubernetes automatically adds more Pods to handle the load.
// If traffic decreases, Kubernetes removes extra Pods to save resources.

// 3. Load Balancing
// If one server gets too many requests, Kubernetes spreads the load to keep things running smoothly.

// 4. Self-Healing
// If a Pod crashes, Kubernetes automatically restarts it or replaces it with a new one.

// 5. Deployment & Updates
// You can update your app without downtime by rolling out changes gradually.
// If something goes wrong, Kubernetes can roll back to the previous version.





