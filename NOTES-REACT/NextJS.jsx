

//////////// What is NextJS ? //////////////

// Next.js is built on top of React, and it can be thought of as a framework or a set of tools that enhance the functionality of React.
// It's not a separate environment, but rather an extension of the React ecosystem.
// It uses the same React components and APIs. 


// Next.js was created by Vercel (formerly Zeit) to address some of the common pain points of building server-side rendered (SSR) & statically generated websites with React. 
// Some of the key features that Next.js provides include:

// Server-Side Rendering (SSR): Next.js allows you to render your React components on the server, which can improve SEO and provide faster page loads.
// Static Site Generation (SSG): Next.js can generate static HTML files for your pages at build time, which can be served directly by a CDN or web server.
// Routing: Next.js provides a built-in routing system, which makes it easy to handle client-side and server-side routing.
// Internationalization (i18n) and Localization (L10n): Next.js provides built-in support for internationalization and localization.
// API Routes: Next.js allows you to create API routes, which can be used to handle server-side logic and provide data to your React components.
// Built-in Support for TypeScript: Next.js has built-in support for TypeScript, which makes it easy to use TypeScript in your projects.

