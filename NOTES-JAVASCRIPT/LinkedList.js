
// A linked list is a data structure used to store a collection of elements (called nodes). Each node contains two main parts:

// Data: The actual value stored in the node.
// Next: A pointer/reference to the next node in the list.

// Unlike arrays, linked lists do not store elements in contiguous memory locations. Instead, each node points to the next node, forming a chain.

// TYPES OF LINKED LISTS: 
//1. Singly Linked List: Each node has one pointer that points to the next node.
//2. Doubly Linked List: Each node has two pointers, one pointing to the next node and one to the previous node.
//3. Circular Linked List: The last node points back to the first node, forming a loop.
